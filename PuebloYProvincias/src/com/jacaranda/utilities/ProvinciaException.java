package com.jacaranda.utilities;

public class ProvinciaException extends Exception {

	public ProvinciaException() {
	}

	public ProvinciaException(String message) {
		super(message);
	}

	public ProvinciaException(Throwable cause) {
		super(cause);
	}

	public ProvinciaException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProvinciaException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
